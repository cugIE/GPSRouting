package com.bean;

public class Faultmsg {
	private String id;
	private String faultTitle;
	private String faultWord;
	private String faultUrL;
	private String videoUrl;
	private String faultTime;
	private String faultState;
	private String dutyPeople;
	private String generId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFaultTitle() {
		return faultTitle;
	}
	public void setFaultTitle(String faultTitle) {
		this.faultTitle = faultTitle;
	}
	public String getFaultWord() {
		return faultWord;
	}
	public void setFaultWord(String faultWord) {
		this.faultWord = faultWord;
	}
	public String getFaultUrL() {
		return faultUrL;
	}
	public void setFaultUrL(String faultUrL) {
		this.faultUrL = faultUrL;
	}
	public String getVideoUrl() {
		return videoUrl;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	public String getFaultTime() {
		return faultTime;
	}
	public void setFaultTime(String faultTime) {
		this.faultTime = faultTime;
	}
	public String getFaultState() {
		return faultState;
	}
	public void setFaultState(String faultState) {
		this.faultState = faultState;
	}
	public String getDutyPeople() {
		return dutyPeople;
	}
	public void setDutyPeople(String dutyPeople) {
		this.dutyPeople = dutyPeople;
	}
	public String getGenerId() {
		return generId;
	}
	public void setGenerId(String generId) {
		this.generId = generId;
	}
	
	
}
