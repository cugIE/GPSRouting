package com.bean;

public class Site {
	private String id;
	private String webtitle;
	private String webad;
	private String webword;
	private String comname;
	private String generid;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWebtitle() {
		return webtitle;
	}
	public void setWebtitle(String webtitle) {
		this.webtitle = webtitle;
	}
	public String getWebad() {
		return webad;
	}
	public void setWebad(String webad) {
		this.webad = webad;
	}
	public String getWebword() {
		return webword;
	}
	public void setWebword(String webword) {
		this.webword = webword;
	}
	public String getComname() {
		return comname;
	}
	public void setComname(String comname) {
		this.comname = comname;
	}
	public String getGenerid() {
		return generid;
	}
	public void setGenerid(String generid) {
		this.generid = generid;
	}
	
}
